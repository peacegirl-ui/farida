-- #QUERIES TO PERFORM:-


-- 1) INNERJOIN:- RETRIVE ALL ORDERS AND COSTOMER DETAILS WHERE ORDERS EXIST.
SELECT*
FROM ORDERS
INNER JOIN COSTOMERS 
ON ORDERS.COSTOMERID=COSTOMERS.COSTOMER_ID;

-- 2) LEFTJOIN:- RETRIVE ALL COSTOMERS AND THEIR CORROSPONDING ORDERS.
SELECT*
FROM COSTOMERS
LEFT JOIN ORDERS
ON COSTOMERS.COSTOMER_ID = ORDERS.COSTOMERID;

-- 3) RETRIVE ALL ORDERS AND THEIR CORROSPONDING COSTOMERS - RIGHT JOIN.
SELECT*
FROM COSTOMERS
RIGHT JOIN ORDERS 
ON COSTOMERS.COSTOMER_ID = ORDERS.COSTOMERID;

-- 4) FULL OUTER JOIN - RETRIVE ALKL COSTOMERS AND ALL ORDERS REGARDLESS OF MATCHING.
SELECT*
FROM COSTOMERS
LEFT JOIN ORDERS 
ON COSTOMERS.COSTOMER_ID = ORDERS.COSTOMERID

UNION

SELECT*
FROM COSTOMERS 
RIGHT JOIN ORDERS 
ON COSTOMERS.COSTOMER_ID = ORDERS.COSTOMERID;

-- 5) EXTRACT THE YEAR AND MONTH FROM THE ORDER DATE.
SELECT
YEAR(ORDERDATE) AS YEAR,
MONTHNAME(ORDERDATE) AS MONTH

FROM ORDERS;

-- 6) Calculate the difference in days between two dates: ORDER_DATE and CURRENT_DATE.
SELECT
  ORDERDATE,
  CURRENT_DATE() AS CURRENTDATE,
  DATEDIFF(CURRENT_DATE() , ORDERDATE) AS DAY_DIFFERENCE

FROM ORDERS;

-- 7)FORMAT THE ORDER DATE TO A MORE READABLE FORMAT.
SELECT
DATE_FORMAT( ORDERDATE , "%d-%b-%y") AS ORDERDATE
FROM ORDERS;

-- 8)Concatenate last name and first name to form a full name.

SELECT 
    CONCAT(FIRSTNAME, ' ', LASTNAME) AS FULL_NAME
FROM COSTOMERS;

-- 9)Replace path of string. Replace John to Johnathan.

SELECT 
    REPLACE(FIRSTNAME , 'JOHN' , 'JOHNATHAN' ) AS UPDATED_NAME 
FROM COSTOMERS;

-- 10) Convert first name to uppercase and last name to lowercase.
SELECT
UPPER (FIRSTNAME) AS FIRST_NAME,
LOWER (LASTNAME) AS LAST_NAME

FROM COSTOMERS;


-- 11) TRIM EXTRA SPACES FROM THE EMAIL FIELD.
SELECT
TRIM(EMAIL) AS CLEAN_NAME
FROM COSTOMERS;

-- 12) Calculate the running total of the total amount of each order.
SELECT 
ORDERID,
TOTALAMOUNT,
SUM(TOTALAMOUNT) OVER (ORDER BY ORDERID) AS RUNNING_TOTAL

FROM ORDERS;

-- 13) Rank orders based on total amount using the rank() function.
SELECT 
ORDERID,
TOTALAMOUNT,
RANK() OVER (ORDER BY TOTALAMOUNT DESC) AS ORDER_RANK

FROM ORDERS;

--14) Assign a discount based on total amount in orders. 
SELECT
EMPLOYEE_ID,
FIRSTNAME,
LASTNAME,
DEPARTMENT,
SALARY,
CASE
WHEN SALARY  > 50000 THEN SALARY *0.10
WHEN SALARY  > 45000 THEN SALARY *0.05
ELSE 0
  END AS BONUS
FROM EMPLOYEES;

--  15) Categorize employee salaries as high, medium, and or low.
SELECT
  EMPLOYEE_ID,
  FIRSTNAME,
  LASTNAME,
  DEPARTMENT,
  SALARY,
  CASE
  WHEN SALARY > 65000 THEN 'HIGH'
  WHEN SALARY BETWEEN 40000 AND 60000 THEN 'MEDIUM'
   ELSE 'LOW' 
    END AS SALARY_CATEGORISATION
    
 FROM EMPLOYEES;

--  16) Subquery to find customers who have placed orders worth more than average amount.
SELECT DISTINCT COSTOMERID
FROM ORDERS
WHERE TOTALAMOUNT >
  (SELECT AVG(TOTALAMOUNT) FROM ORDERS);
    
-- 17) Subquery to find employees with salaries above the average salary.
SELECT
EMPLOYEE_ID,
FIRSTNAME,
LASTNAME,
SALARY
FROM EMPLOYEES
WHERE SALARY >
  (SELECT AVG(SALARY) FROM EMPLOYEES);
